def test_parse_bool_and_resolve_include_thoughts(
    monkeypatch: pytest.MonkeyPatch, tmp_path: Path
) -> None:
    service = _new_service(monkeypatch, tmp_path)

    assert service._parse_bool(" true ", False) is True
    assert service._parse_bool("OFF", True) is False
    assert service._parse_bool("unknown", True) is True

    monkeypatch.setenv("GEMINI_INCLUDE_THOUGHTS", "0")
    assert service._resolve_include_thoughts(True) is False
    monkeypatch.setenv("GEMINI_INCLUDE_THOUGHTS", "yes")
    assert service._resolve_include_thoughts(False) is True


