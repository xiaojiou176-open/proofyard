def test_execute_with_playwright_normalizes_evidence_fields(
    monkeypatch: pytest.MonkeyPatch, tmp_path: Path
) -> None:
    service = _new_service(monkeypatch, tmp_path)
    session = service.create_session(instruction="open", actor="alice")
    action = _new_action(args={"key": "value"})

    script = tmp_path / "executor.mjs"
    script.write_text("// stub", encoding="utf-8")
    service._playwright_executor_script = script

    payload = {
        "executor": "node-worker",
        "evidence": {
            "screens": "wrong",
            "clips": ["clip.mp4"],
            "network_summary": [],
            "dom_summary": {"title": "ok"},
            "replay_trace": "bad",
        },
    }
    monkeypatch.setattr(
        computer_use_service_module.subprocess,
        "run",
        lambda *a, **k: SimpleNamespace(returncode=0, stdout=json.dumps(payload), stderr=""),
    )

    result = service._execute_with_playwright(session=session, action=action, actor="alice")

    assert result["executor"] == "node-worker"
    assert result["evidence"] == {
        "screens": [],
        "clips": ["clip.mp4"],
        "network_summary": {},
        "dom_summary": {"title": "ok"},
        "replay_trace": {},
    }


def test_require_session_owned_session_and_action_branches(
    monkeypatch: pytest.MonkeyPatch, tmp_path: Path
) -> None:
    service = _new_service(monkeypatch, tmp_path)
    session = service.create_session(instruction="open", actor="owner")

    with pytest.raises(ComputerUseServiceError) as invalid_id:
        service._require_session("bad-id")
    assert invalid_id.value.status_code == 422

    with pytest.raises(ComputerUseServiceError) as missing:
        service._require_session("cus_" + "f" * 32)
    assert missing.value.status_code == 404

    with pytest.raises(ComputerUseServiceError) as ownership:
        service._require_owned_session(session.session_id, "other")
    assert ownership.value.status_code == 404

    with pytest.raises(ComputerUseServiceError) as action_missing:
        service._require_action(session.session_id, "act_missing", "owner")
    assert action_missing.value.status_code == 404


def test_read_evidence_skips_bad_lines(monkeypatch: pytest.MonkeyPatch, tmp_path: Path) -> None:
    service = _new_service(monkeypatch, tmp_path)
    session = service.create_session(instruction="open", actor="alice")
    evidence_file = service._evidence_file(session.session_id)

    with evidence_file.open("a", encoding="utf-8") as handle:
        handle.write("not-json\n")
        handle.write(json.dumps(["array-not-dict"]) + "\n")
        handle.write("\n")
        handle.write(json.dumps({"event": "custom", "payload": {"ok": True}}) + "\n")

    data = service.read_evidence(session_id=session.session_id, actor="alice")

    assert data["sessionId"] == session.session_id
    assert data["eventCount"] == 2
    assert data["events"][0]["event"] == "session_created"
    assert data["events"][1]["event"] == "custom"
