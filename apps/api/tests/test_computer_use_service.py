def test_extract_action_from_response_prefers_function_call(
    monkeypatch: pytest.MonkeyPatch, tmp_path: Path
) -> None:
    service = _new_service(monkeypatch, tmp_path)
    response = SimpleNamespace(
        candidates=[
            SimpleNamespace(
                content=SimpleNamespace(
                    parts=[
                        SimpleNamespace(function_call=SimpleNamespace(name="click", args={"x": 12}))
                    ]
                )
            )
        ]
    )

    name, args, rationale = service._extract_action_from_response(response)

    assert name == "click"
    assert args == {"x": 12}
    assert rationale == "generated by gemini computer-use planner"


