def test_classify_risk_branches(monkeypatch: pytest.MonkeyPatch, tmp_path: Path) -> None:
    service = _new_service(monkeypatch, tmp_path)

    critical = service._classify_risk("delete", {}, "safe")
    high = service._classify_risk("click", {"note": "send now"}, "open page")
    medium = service._classify_risk("click", {}, "open page")
    low = service._classify_risk("inspect", {}, "open page")

    assert critical == ("critical", "action 'delete' is high impact")
    assert high == ("high", "instruction/args contain high-risk keywords")
    assert medium == ("medium", None)
    assert low == ("low", None)


