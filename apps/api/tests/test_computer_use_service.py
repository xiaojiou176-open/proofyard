def test_execute_with_playwright_subprocess_exception(
    monkeypatch: pytest.MonkeyPatch, tmp_path: Path
) -> None:
    service = _new_service(monkeypatch, tmp_path)
    session = service.create_session(instruction="open", actor="alice")
    action = _new_action()

    script = tmp_path / "executor.mjs"
    script.write_text("// stub", encoding="utf-8")
    service._playwright_executor_script = script

    def _raise(*args, **kwargs):
        raise OSError("node unavailable")

    monkeypatch.setattr(computer_use_service_module.subprocess, "run", _raise)

